// ═══════════════════════════════════════════════════════
// PROMPT CACHING LAYER (Token Optimization #2)
// ═══════════════════════════════════════════════════════
const PROMPT_CACHE = {
  basePrompt: null,
  language: null,
};

// Clear cache when language changes
onConfigChange('general.uiLocale', () => {
  PROMPT_CACHE.basePrompt = null;
  PROMPT_CACHE.language = null;
  console.log('[PROMPT-CACHE] Cleared due to language change');
});

/** Get language instruction dynamically based on current config */
function getLangInstruction() {
  try { return getPromptFragments().agentLang; } catch { return "Always respond to the user in English. Respond in ONE language only — never include a translation in a second language."; }
}

/** Get base prompt with dynamic language instruction (CACHED) */
export function getBasePrompt() {
  const currentLang = getConfig('general.uiLocale') || 'en';

  // Return cached version if language hasn't changed
  if (PROMPT_CACHE.basePrompt && PROMPT_CACHE.language === currentLang) {
    return PROMPT_CACHE.basePrompt;
  }

  // Generate fresh
  const pf = getPromptFragments();
  const port = process.env.PORT || 3000;
  const basePrompt = `${pf.autonomyIntro}
${pf.agentLang}

${pf.basePromptRules}`.replace(/http:\/\/localhost:3000/g, `http://localhost:${port}`);

  // Cache it
  PROMPT_CACHE.basePrompt = basePrompt;
  PROMPT_CACHE.language = currentLang;

  return basePrompt;
}

// Keep BASE_PROMPT as a static export for backward compatibility (French fallback)
export const BASE_PROMPT = getBasePrompt();

/**
 * Build a complete system prompt for an agent.
 * roleInstructions is free-form text generated by Yabby at agent creation time.
 */
export function buildAgentPrompt(agentName, role, roleInstructions, projectContext) {
  const pf = getPromptFragments();
  const parts = [getBasePrompt()];

  parts.push(`\n${pf.yourIdentity}:\n${pf.identity(agentName, role)}`);

  if (roleInstructions) {
    parts.push(`\n${pf.roleInstructions}:\n${roleInstructions}`);
  }

  if (projectContext) {
    parts.push(`\n${pf.projectContext}:\n${projectContext}`);
  }

  return parts.join("\n");
}

/**
 * Build a runtime context block injected into the system prompt of an agent.
 * Contains info that changes over time and should NOT be stored in the static
 * system_prompt column (workspace path, current date, etc.).
 *
 * Used by both spawner.js (CLI) and handler.js (channel LLM1) so both sides
 * know the exact working directory and agent identity at any given moment.
 *
 * @param {object} opts
 * @param {string} opts.name
 * @param {string} opts.role
 * @param {string} opts.agentId
 * @param {string} [opts.workspacePath] - current CWD override (from DB)
 * @param {string} [opts.defaultWorkspace] - resolved default workspace if no override
 * @returns {string}
 */
export function buildAgentContextBlock({ name, role, agentId, workspacePath, defaultWorkspace }) {
  const now = new Date().toLocaleString('en-US', { dateStyle: 'full', timeStyle: 'short', timeZone: 'Europe/Paris' });
  const cwd = workspacePath || defaultWorkspace || '(workspace not resolved)';
  const isOverride = !!workspacePath;

  return `
# CURRENT CONTEXT

- Name: ${name}
- Role: ${role}
- ID: ${agentId}
- Date: ${now}
- Working directory: \`${cwd}\`${isOverride ? ' (set via change-workspace)' : ' (default)'}
`;
}

